#include <stdlib.h>

#include "bmp.h"
#include "macro.h"

enum {
	WIDTH = 4,
	HEIGHT = 2,
};

static const bmp_pixel32 BUF[] = {
	{0xFF, 0x00, 0x00, 0x7F},
	{0x00, 0xFF, 0x00, 0x7F},
	{0x00, 0x00, 0xFF, 0x7F},
	{0xFF, 0xFF, 0xFF, 0x7F},
	{0xFF, 0x00, 0x00, 0xFF},
	{0x00, 0xFF, 0x00, 0xFF},
	{0x00, 0x00, 0xFF, 0xFF},
	{0xFF, 0xFF, 0xFF, 0xFF},
};

static const char *const BMP_FILE = "./assets/test.bmp";

int main(void)
{
	extern const bmp_pixel32 BUF[];
	extern const char *const BMP_FILE;

	return (bmp_v4_write(BUF, WIDTH, HEIGHT, BMP_FILE) == 0)
		? EXIT_SUCCESS
		: EXIT_FAILURE;
}
